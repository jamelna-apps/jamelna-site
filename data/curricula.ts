export const getGradeLevelLabel = (level: GradeLevel): string => {
  const labels: Record<GradeLevel, string> = {
    elementary: 'Elementary (K-5)',
    middle: 'Middle School (6-8)',
    high: 'High School (9-12)',
    all: 'All Grades (K-12)',
  };
  return labels[level];
};

export const getTopicLabel = (topic: Topic): string => {
  const labels: Record<Topic, string> = {
    cs: 'Computer Science',
    ct: 'Computational Thinking',
    ai: 'Artificial Intelligence',
    cybersecurity: 'Cybersecurity',
    robotics: 'Robotics',
    data: 'Data Science',
    web: 'Web Development',
  };
  return labels[topic];
};

export const filterByGradeLevel = (curricula: Curriculum[], level: GradeLevel): Curriculum[] => {
  if (level === 'all') return curricula;
  return curricula.filter(c => c.grades.includes(level));
};

export const filterByTopic = (curricula: Curriculum[], topic: Topic): Curriculum[] => {
  return curricula.filter(c => c.topics.includes(topic));
};
