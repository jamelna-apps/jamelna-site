// Helper functions
export function getStateByAbbreviation(abbreviation: string): StatePolicy | undefined {
  return statePolicies.find(
    (s) => s.abbreviation.toLowerCase() === abbreviation.toLowerCase()
  );
}

export function getStateByName(name: string): StatePolicy | undefined {
  return statePolicies.find(
    (s) => s.state.toLowerCase() === name.toLowerCase()
  );
}

export function getStatesWithStandards(): StatePolicy[] {
  return statePolicies.filter((s) => s.hasK12Standards);
}

export function getStatesWithFunding(): StatePolicy[] {
  return statePolicies.filter((s) => s.hasDedicatedFunding);
}

export function getStatesWithPreserviceReq(): StatePolicy[] {
  return statePolicies.filter((s) => s.hasPreserviceReq);
}

export function formatStatePolicyForRAG(policy: StatePolicy): string {
  const policies = [];

  if (policy.hasK12Standards) {
    policies.push(`K-12 Standards: Yes${policy.k12StandardsDetails ? ` - ${policy.k12StandardsDetails}` : ''}`);
  } else {
    policies.push('K-12 Standards: No');
  }

  if (policy.csCountsForGraduation) {
    policies.push(`CS Graduation Credit: Yes${policy.graduationDetails ? ` - ${policy.graduationDetails}` : ''}`);
  } else {
    policies.push('CS Graduation Credit: No');
  }

  if (policy.hasCSCertification) {
    policies.push(`Teacher Certification: Yes${policy.certificationDetails ? ` - ${policy.certificationDetails}` : ''}`);
  } else {
    policies.push('Teacher Certification: No');
  }

  if (policy.hasDedicatedFunding) {
    policies.push(`Dedicated Funding: Yes${policy.fundingDetails ? ` - ${policy.fundingDetails}` : ''}`);
  } else {
    policies.push('Dedicated Funding: No');
  }

  if (policy.hasPreserviceReq) {
    policies.push(`Preservice Requirement: Yes${policy.preserviceDetails ? ` - ${policy.preserviceDetails}` : ''}`);
  }

  return `${policy.state} (${policy.abbreviation}) CS Education Policy:
${policies.join('\n')}${policy.additionalNotes ? `\n\nAdditional Notes: ${policy.additionalNotes}` : ''}`;
}

/**
 * Generate all state policy documents for RAG
 */
export function generateStatePolicyRAGDocuments(): Array<{
  id: string;
  docType: 'state_policy';
  content: string;
  metadata: {
    state: string;
    abbreviation: string;
    hasK12Standards: boolean;
    csCountsForGraduation: boolean;
    hasCSCertification: boolean;
    hasDedicatedFunding: boolean;
  };
}> {
  return statePolicies.map((policy) => ({
    id: `state-policy-${policy.abbreviation.toLowerCase()}`,
    docType: 'state_policy' as const,
    content: formatStatePolicyForRAG(policy),
    metadata: {
      state: policy.state,
      abbreviation: policy.abbreviation,
      hasK12Standards: policy.hasK12Standards,
      csCountsForGraduation: policy.csCountsForGraduation,
      hasCSCertification: policy.hasCSCertification,
      hasDedicatedFunding: policy.hasDedicatedFunding,
    },
  }));
}
